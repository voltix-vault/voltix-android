private const val HideChars = "****"

@Composable
internal fun ToggleVisibilityText(
    modifier: Modifier = Modifier,
    isVisible: Boolean,
    onChangeVisibilityButtonClick: () -> Unit,
    text: String,
    color: Color = Color.Unspecified,
    fontSize: TextUnit = TextUnit.Unspecified,
    fontStyle: FontStyle? = null,
    fontWeight: FontWeight? = null,
    fontFamily: FontFamily? = null,
    letterSpacing: TextUnit = TextUnit.Unspecified,
    textDecoration: TextDecoration? = null,
    textAlign: TextAlign? = null,
    lineHeight: TextUnit = TextUnit.Unspecified,
    overflow: TextOverflow = TextOverflow.Clip,
    softWrap: Boolean = true,
    maxLines: Int = Int.MAX_VALUE,
    minLines: Int = 1,
    onTextLayout: ((TextLayoutResult) -> Unit)? = null,
    style: TextStyle = LocalTextStyle.current
) {
    AnimatedContent(
        targetState = isVisible,
        label = "toggle value visibility"
    ) {
        Row(
            verticalAlignment = Alignment.CenterVertically,
            horizontalArrangement = Arrangement.spacedBy(4.dp)
        ) {
            Text(
                text = if (it) text else HideChars,
                modifier = modifier,
                color = color,
                fontSize = fontSize,
                fontStyle = fontStyle,
                fontWeight = fontWeight,
                fontFamily = fontFamily,
                letterSpacing = letterSpacing,
                textDecoration = textDecoration,
                textAlign = textAlign,
                lineHeight = lineHeight,
                overflow = overflow,
                softWrap = softWrap,
                maxLines = maxLines,
                minLines = minLines,
                onTextLayout = onTextLayout,
                style = style,
            )

            IconButton(onClick = onChangeVisibilityButtonClick) {
                Icon(
                    modifier = Modifier.width(28.dp),
                    painter = painterResource(
                        id = if (it) R.drawable.visible else R.drawable.hidden
                    ),
                    tint = color,
                    contentDescription = "change visibility button"
                )
            }
        }
    }
}


